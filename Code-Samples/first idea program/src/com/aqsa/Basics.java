package com.aqsa;

public class Basics {
    public static void main(String[] args) {
        if (true) {
            System.out.println("hey lol");
        }

        int a = 10;
        if (a == 10) {
            System.out.println("doritossssss");
        }

        int count = 1;
        while(count != 7) {
            System.out.println(count);
            count++;
        }

        //for loop
        for(int vari = 2; vari != 6; vari++) {
            System.out.println(vari);
        }

        //USAGE OF WHILE AND FOR
//        when you don't know how many times the loop is going to run, use while loop
//        when you know how many times the loop is going to run, use for loop
    }
}
